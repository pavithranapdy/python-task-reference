
#ARITHMETIC OPERATOR
x=25
y=13
print(x,"+",y,"= ",x+y)#addition
print(x,"-",y,"= ",x-y)#subratction
print(x,"X",y,"= ",x*y)#multiplication
print(x,"/",y,"= ",x/y)#division
print(x,"%",y,"= ",x%y)#modulus / reminder
print(x,"**",y,"= ",x**y)#exponentiation / increase power
print(x,"//",y,"= ",x//y)#floor division/ rounded value 

